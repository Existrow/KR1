using System;
using System.Collections.Generic;
using System.Linq;
using System.Runtime.Serialization;
using System.ServiceModel;
using System.Text;

namespace WcfServiceLibrary1
{
    // ПРИМЕЧАНИЕ. Команду "Переименовать" в меню "Рефакторинг" можно использовать для одновременного изменения имени класса "Service1" в коде и файле конфигурации.
    public class Service1 : IService1
    {
        x002E_csContainer Data = new x002E_csContainer();
        public List<Request> GetData() =>
            Data.RequestSet.ToList();

        public void SetData(DateTime date, string autor, string description)
        {
            Data.RequestSet.Add(new Request() { Date = date, Autor = autor, Description = description });
            Data.SaveChanges();
        }

        public CompositeType GetDataUsingDataContract(CompositeType composite)
        {
            if (composite == null)
            {
                throw new ArgumentNullException("composite");
            }
            if (composite.BoolValue)
            {
                composite.StringValue += "Suffix";
            }
            return composite;
        }
    }
}
